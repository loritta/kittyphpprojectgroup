                    <p class="text">
                        Cats with drooping ears or folded ears are ancient. The first word of one goes as far back as 1796. In this year, an English sailor brought back one of these cats when he returned from China. The Chinese also contributed the second example of a folded ear cat to the cat fancy history when one was noted in 1938.</br>

                        But the Scottish Fold as we know her today was first born in 1961 in Pertshire, Scotland. There, a kitten with folded ears was discovered in a litter of otherwise "normal" eared kittens. This kitten, named Susie, was then bred to domestic cats and British Shorthair cats to establish the folded ear. To this day, every Scottish fold can trace her ancestry to Susie.</br>

